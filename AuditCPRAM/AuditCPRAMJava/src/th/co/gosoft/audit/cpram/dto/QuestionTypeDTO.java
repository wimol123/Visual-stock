package th.co.gosoft.audit.cpram.dto;

import java.util.List;

public class QuestionTypeDTO extends StandardAttributeDTO {
	private int questionTypeId;
	private String name; 
	private List<AnswerDTO> answerId;
	private List<EvalFormDTO> evalFormId;
	private List<ChecklistTypeDTO> checklistTypeId;
	
	public int getQuestionTypeId() {
		return questionTypeId;
	}
	public void setQuestionTypeId(int questionTypeId) {
		this.questionTypeId = questionTypeId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public List<AnswerDTO> getAnswerId() {
		return answerId;
	}
	public void setAnswerId(List<AnswerDTO> answerId) {
		this.answerId = answerId;
	}
	public List<EvalFormDTO> getEvalFormId() {
		return evalFormId;
	}
	public void setEvalFormId(List<EvalFormDTO> evalFormId) {
		this.evalFormId = evalFormId;
	}
	public List<ChecklistTypeDTO> getChecklistTypeId() {
		return checklistTypeId;
	}
	public void setChecklistTypeId(List<ChecklistTypeDTO> checklistTypeId) {
		this.checklistTypeId = checklistTypeId;
	}
}
