package th.co.gosoft.audit.cpram.dto;

import java.util.List;

public class EvalTypeDTO extends StandardAttributeDTO {
	private int evalTypeId;
	private String evalTypeName;
	private List<EvalFormDTO> evalFormId;
	public int getEvalTypeId() {
		return evalTypeId;
	}
	public void setEvalTypeId(int evalTypeId) {
		this.evalTypeId = evalTypeId;
	}
	public String getEvalTypeName() {
		return evalTypeName;
	}
	public void setEvalTypeName(String evalTypeName) {
		this.evalTypeName = evalTypeName;
	}
	public List<EvalFormDTO> getEvalFormId() {
		return evalFormId;
	}
	public void setEvalFormId(List<EvalFormDTO> evalFormId) {
		this.evalFormId = evalFormId;
	}
}
