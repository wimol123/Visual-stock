package th.co.gosoft.audit.cpram.model;

import java.util.List;

public class QuestionTypeModel extends StandardAttributeModel {
	private String questionTypeId;
	private String name;
	private List<AnswerModel> answerId;
	private List<EvalFormModel> evalFormId;
	private List<ChecklistTypeModel> checklistTypeId;
	
	public String getQuestionTypeId() {
		return questionTypeId;
	}
	public void setQuestionTypeId(String questionTypeId) {
		this.questionTypeId = questionTypeId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public List<AnswerModel> getAnswerId() {
		return answerId;
	}
	public void setAnswerId(List<AnswerModel> answerId) {
		this.answerId = answerId;
	}
	public List<EvalFormModel> getEvalFormId() {
		return evalFormId;
	}
	public void setEvalFormId(List<EvalFormModel> evalFormId) {
		this.evalFormId = evalFormId;
	}
	public List<ChecklistTypeModel> getChecklistTypeId() {
		return checklistTypeId;
	}
	public void setChecklistTypeId(List<ChecklistTypeModel> checklistTypeId) {
		this.checklistTypeId = checklistTypeId;
	}
}
