package th.co.gosoft.audit.cpram.model;

public class SystemSequenceModel extends StandardAttributeModel{
	private String seqKey;
	private String seqValue; 
	private String seqDigits; 
	private String useKeyPrefix; 
	private String delimit; 
	private String seqDescription;
	public String getSeqKey() {
		return seqKey;
	}
	public void setSeqKey(String seqKey) {
		this.seqKey = seqKey;
	}
	public String getSeqValue() {
		return seqValue;
	}
	public void setSeqValue(String seqValue) {
		this.seqValue = seqValue;
	}
	public String getSeqDigits() {
		return seqDigits;
	}
	public void setSeqDigits(String seqDigits) {
		this.seqDigits = seqDigits;
	}
	public String getUseKeyPrefix() {
		return useKeyPrefix;
	}
	public void setUseKeyPrefix(String useKeyPrefix) {
		this.useKeyPrefix = useKeyPrefix;
	}
	public String getDelimit() {
		return delimit;
	}
	public void setDelimit(String delimit) {
		this.delimit = delimit;
	}
	public String getSeqDescription() {
		return seqDescription;
	}
	public void setSeqDescription(String seqDescription) {
		this.seqDescription = seqDescription;
	}
	
	
}
