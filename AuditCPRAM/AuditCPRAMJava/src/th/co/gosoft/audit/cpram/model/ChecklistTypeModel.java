package th.co.gosoft.audit.cpram.model;

import java.util.List;

public class ChecklistTypeModel extends StandardAttributeModel{
	private String checklistTypeId;
	private String checklistTypeName;	
	private List<ChecklistModel> checklistId;
	private List<QuestionTypeModel> questionTypeId;
	private List<GradeModel> gradeId;
	
	public String getChecklistTypeId() {
		return checklistTypeId;
	}
	public void setChecklistTypeId(String checklistTypeId) {
		this.checklistTypeId = checklistTypeId;
	}
	public String getChecklistTypeName() {
		return checklistTypeName;
	}
	public void setChecklistTypeName(String checklistTypeName) {
		this.checklistTypeName = checklistTypeName;
	}
	public List<ChecklistModel> getChecklistId() {
		return checklistId;
	}
	public void setChecklistId(List<ChecklistModel> checklistId) {
		this.checklistId = checklistId;
	}
	public List<QuestionTypeModel> getQuestionTypeId() {
		return questionTypeId;
	}
	public void setQuestionTypeId(List<QuestionTypeModel> questionTypeId) {
		this.questionTypeId = questionTypeId;
	}
	public List<GradeModel> getGradeId() {
		return gradeId;
	}
	public void setGradeId(List<GradeModel> gradeId) {
		this.gradeId = gradeId;
	}
}
