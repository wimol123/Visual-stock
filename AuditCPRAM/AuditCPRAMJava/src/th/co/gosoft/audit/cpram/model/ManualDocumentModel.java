package th.co.gosoft.audit.cpram.model;

public class ManualDocumentModel extends StandardAttributeModel{
	
	private String documentid;	
	private String documentlocation;	
	private String document_type;
	/**
	 * @return the documentid
	 */
	public String getDocumentid() {
		return documentid;
	}
	/**
	 * @param documentid the documentid to set
	 */
	public void setDocumentid(String documentid) {
		this.documentid = documentid;
	}
	/**
	 * @return the documentlocation
	 */
	public String getDocumentlocation() {
		return documentlocation;
	}
	/**
	 * @param documentlocation the documentlocation to set
	 */
	public void setDocumentlocation(String documentlocation) {
		this.documentlocation = documentlocation;
	}
	/**
	 * @return the document_type
	 */
	public String getDocument_type() {
		return document_type;
	}
	/**
	 * @param document_type the document_type to set
	 */
	public void setDocument_type(String document_type) {
		this.document_type = document_type;
	}

	



}
