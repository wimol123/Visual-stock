package th.co.gosoft.audit.cpram.model;

import java.util.List;

public class ProductTypeModel extends StandardAttributeModel{
	private String productTypeId;
	private String name;	
	private ChecklistModel checklistId;
	private List<SupplierProductAddressMappingModel> supplierProductAddressMappingModel;
	
	public String getProductTypeId() {
		return productTypeId;
	}
	public void setProductTypeId(String productTypeId) {
		this.productTypeId = productTypeId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public ChecklistModel getChecklistId() {
		return checklistId;
	}
	public void setChecklistId(ChecklistModel checklistId) {
		this.checklistId = checklistId;
	}
	public List<SupplierProductAddressMappingModel> getSupplierProductAddressMappingModel() {
		return supplierProductAddressMappingModel;
	}
	public void setSupplierProductAddressMappingModel(
			List<SupplierProductAddressMappingModel> supplierProductAddressMappingModel) {
		this.supplierProductAddressMappingModel = supplierProductAddressMappingModel;
	}
	
	
}
