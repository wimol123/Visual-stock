package th.co.gosoft.audit.cpram.dto;

public class SystemSequenceDTO extends StandardAttributeDTO{
	private String seqKey;
	private int seqValue; 
	private int seqDigits; 
	private Character useKeyPrefix; 
	private Character delimit; 
	private String seqDescription;
	public String getSeqKey() {
		return seqKey;
	}
	public void setSeqKey(String seqKey) {
		this.seqKey = seqKey;
	}
	public int getSeqValue() {
		return seqValue;
	}
	public void setSeqValue(int seqValue) {
		this.seqValue = seqValue;
	}
	public int getSeqDigits() {
		return seqDigits;
	}
	public void setSeqDigits(int seqDigits) {
		this.seqDigits = seqDigits;
	}
	public Character getUseKeyPrefix() {
		return useKeyPrefix;
	}
	public void setUseKeyPrefix(Character useKeyPrefix) {
		this.useKeyPrefix = useKeyPrefix;
	}
	public Character getDelimit() {
		return delimit;
	}
	public void setDelimit(Character delimit) {
		this.delimit = delimit;
	}
	public String getSeqDescription() {
		return seqDescription;
	}
	public void setSeqDescription(String seqDescription) {
		this.seqDescription = seqDescription;
	}
	
	
}
