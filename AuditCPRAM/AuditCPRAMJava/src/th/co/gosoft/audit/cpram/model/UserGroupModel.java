package th.co.gosoft.audit.cpram.model;

import java.util.List;

public class UserGroupModel extends StandardAttributeModel {
	private String userGroupId;
	private String userGroupName; 
	private String authenType;
	private List<UserModel> userId;
	private List<MenuModel> userGroupMenuId;
	public String getUserGroupId() {
		return userGroupId;
	}
	public void setUserGroupId(String userGroupId) {
		this.userGroupId = userGroupId;
	}
	public String getUserGroupName() {
		return userGroupName;
	}
	public void setUserGroupName(String userGroupName) {
		this.userGroupName = userGroupName;
	}
	public String getAuthenType() {
		return authenType;
	}
	public void setAuthenType(String authenType) {
		this.authenType = authenType;
	}
	public List<UserModel> getUserId() {
		return userId;
	}
	public void setUserId(List<UserModel> userId) {
		this.userId = userId;
	}
	public List<MenuModel> getUserGroupMenuId() {
		return userGroupMenuId;
	}
	public void setUserGroupMenuId(List<MenuModel> userGroupMenuId) {
		this.userGroupMenuId = userGroupMenuId;
	}
}
