package th.co.gosoft.audit.cpram.dto;

import java.util.Date;

public class DistrictDTO {
	private int districtId;
	private String name;
	private String postcode;
	private RegionDTO regionId;
	private ProvinceDTO provinceId;
	private int createBy;
	private Date createDate;
	private int updateBy;
	private Date updateDate;
	public int getDistrictId() {
		return districtId;
	}
	public void setDistrictId(int districtId) {
		this.districtId = districtId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPostcode() {
		return postcode;
	}
	public void setPostcode(String postcode) {
		this.postcode = postcode;
	}
	
	public RegionDTO getRegionId() {
		return regionId;
	}
	public void setRegionId(RegionDTO regionId) {
		this.regionId = regionId;
	}
	public ProvinceDTO getProvinceId() {
		return provinceId;
	}
	public void setProvinceId(ProvinceDTO provinceId) {
		this.provinceId = provinceId;
	}
	public int getCreateBy() {
		return createBy;
	}
	public void setCreateBy(int createBy) {
		this.createBy = createBy;
	}
	public Date getCreateDate() {
		return createDate;
	}
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
	public int getUpdateBy() {
		return updateBy;
	}
	public void setUpdateBy(int updateBy) {
		this.updateBy = updateBy;
	}
	public Date getUpdateDate() {
		return updateDate;
	}
	public void setUpdateDate(Date updateDate) {
		this.updateDate = updateDate;
	}
}
