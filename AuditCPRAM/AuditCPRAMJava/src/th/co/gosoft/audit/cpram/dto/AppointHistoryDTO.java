package th.co.gosoft.audit.cpram.dto;

public class AppointHistoryDTO extends StandardAttributeDTO {
	private int appointHistoryId;
	private AppointDTO appointId;
	private String title;
	private String detail;
	private AppointStatusDTO appointStatusId;
	private UserDTO appointHistoryCreateBy;
	
	public int getAppointHistoryId() {
		return appointHistoryId;
	}
	public void setAppointHistoryId(int appointHistoryId) {
		this.appointHistoryId = appointHistoryId;
	}
	public AppointDTO getAppointId() {
		return appointId;
	}
	public void setAppointId(AppointDTO appointId) {
		this.appointId = appointId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public AppointStatusDTO getAppointStatusId() {
		return appointStatusId;
	}
	public void setAppointStatusId(AppointStatusDTO appointStatusId) {
		this.appointStatusId = appointStatusId;
	}
	public UserDTO getAppointHistoryCreateBy() {
		return appointHistoryCreateBy;
	}
	public void setAppointHistoryCreateBy(UserDTO appointHistoryCreateBy) {
		this.appointHistoryCreateBy = appointHistoryCreateBy;
	}
	
	
	
}
