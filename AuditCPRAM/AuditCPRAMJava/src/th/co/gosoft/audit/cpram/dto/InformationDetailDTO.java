package th.co.gosoft.audit.cpram.dto;

import java.sql.Date;
import java.sql.Time;

public class InformationDetailDTO extends StandardAttributeDTO {
	private int informationDetailId;
	private InformationDTO informationId;
	private SupplierDTO supplierId;
	private UserDTO acceptBy;
	private Character acceptStatus;
	private Date acceptDate;
	private Time acceptTime;
	public int getInformationDetailId() {
		return informationDetailId;
	}
	public void setInformationDetailId(int informationDetailId) {
		this.informationDetailId = informationDetailId;
	}
	public InformationDTO getInformationId() {
		return informationId;
	}
	public void setInformationId(InformationDTO informationId) {
		this.informationId = informationId;
	}
	public SupplierDTO getSupplierId() {
		return supplierId;
	}
	public void setSupplierId(SupplierDTO supplierId) {
		this.supplierId = supplierId;
	}
	public UserDTO getAcceptBy() {
		return acceptBy;
	}
	public void setAcceptBy(UserDTO acceptBy) {
		this.acceptBy = acceptBy;
	}
	public Character getAcceptStatus() {
		return acceptStatus;
	}
	public void setAcceptStatus(Character acceptStatus) {
		this.acceptStatus = acceptStatus;
	}
	public Date getAcceptDate() {
		return acceptDate;
	}
	public void setAcceptDate(Date acceptDate) {
		this.acceptDate = acceptDate;
	}
	public Time getAcceptTime() {
		return acceptTime;
	}
	public void setAcceptTime(Time acceptTime) {
		this.acceptTime = acceptTime;
	}
	
}
