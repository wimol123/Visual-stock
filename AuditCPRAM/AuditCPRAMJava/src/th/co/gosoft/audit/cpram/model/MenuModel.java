package th.co.gosoft.audit.cpram.model;

import java.util.List;

public class MenuModel extends StandardAttributeModel {
	
	private String menuId;
	private String menuParentId;
	private String menuName;
	private String menuUrl;
	private String menuIcon;
	private String menuOrder;
	private String menuSubOrder;
	private String countSubMenu;	
	private List<UserGroupModel> userGroupId;
	
	public String getMenuId() {
		return menuId;
	}
	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}
	public String getMenuParentId() {
		return menuParentId;
	}
	public void setMenuParentId(String menuParentId) {
		this.menuParentId = menuParentId;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getMenuUrl() {
		return menuUrl;
	}
	public void setMenuUrl(String menuUrl) {
		this.menuUrl = menuUrl;
	}
	public String getMenuIcon() {
		return menuIcon;
	}
	public void setMenuIcon(String menuIcon) {
		this.menuIcon = menuIcon;
	}
	public String getMenuOrder() {
		return menuOrder;
	}
	public void setMenuOrder(String menuOrder) {
		this.menuOrder = menuOrder;
	}
	public String getMenuSubOrder() {
		return menuSubOrder;
	}
	public void setMenuSubOrder(String menuSubOrder) {
		this.menuSubOrder = menuSubOrder;
	}
	public String getCountSubMenu() {
		return countSubMenu;
	}
	public void setCountSubMenu(String countSubMenu) {
		this.countSubMenu = countSubMenu;
	}
	public List<UserGroupModel> getUserGroupId() {
		return userGroupId;
	}
	public void setUserGroupId(List<UserGroupModel> userGroupId) {
		this.userGroupId = userGroupId;
	}

}
