package th.co.gosoft.audit.cpram.dto;

public class EvidenceDTO extends StandardAttributeDTO {	
	private int evidenceId;
	private AuditResultDTO auditResultId;
	private EvidenceTypeDTO evidenceTypeId; 
	private String data; 
	private Character actionType;
	public int getEvidenceId() {
		return evidenceId;
	}
	public AuditResultDTO getAuditResultId() {
		return auditResultId;
	}
	public EvidenceTypeDTO getEvidenceTypeId() {
		return evidenceTypeId;
	}
	public String getData() {
		return data;
	}
	public Character getActionType() {
		return actionType;
	}
	public void setEvidenceId(int evidenceId) {
		this.evidenceId = evidenceId;
	}
	public void setAuditResultId(AuditResultDTO auditResultId) {
		this.auditResultId = auditResultId;
	}
	public void setEvidenceTypeId(EvidenceTypeDTO evidenceTypeId) {
		this.evidenceTypeId = evidenceTypeId;
	}
	public void setData(String data) {
		this.data = data;
	}
	public void setActionType(Character actionType) {
		this.actionType = actionType;
	}

}
