package th.co.gosoft.audit.cpram.model;

import java.util.List;

public class AppointTypeModel extends StandardAttributeModel {

	private String appointTypeId; 
	private String name; 
	private List<AppointModel> appointId;
	public String getAppointTypeId() {
		return appointTypeId;
	}
	public void setAppointTypeId(String appointTypeId) {
		this.appointTypeId = appointTypeId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public List<AppointModel> getAppointId() {
		return appointId;
	}
	public void setAppointId(List<AppointModel> appointId) {
		this.appointId = appointId;
	}
	
	
	
}
