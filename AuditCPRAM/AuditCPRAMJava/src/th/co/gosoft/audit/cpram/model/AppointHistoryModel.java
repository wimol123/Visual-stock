package th.co.gosoft.audit.cpram.model;

public class AppointHistoryModel extends StandardAttributeModel {
	private String appointHistoryId;
	private AppointModel appointId;
	private String title;
	private String detail;
	private AppointStatusModel appointStatusId;
	private UserModel appointHistoryCreateBy;
	
	public String getAppointHistoryId() {
		return appointHistoryId;
	}
	public void setAppointHistoryId(String appointHistoryId) {
		this.appointHistoryId = appointHistoryId;
	}
	public AppointModel getAppointId() {
		return appointId;
	}
	public void setAppointId(AppointModel appointId) {
		this.appointId = appointId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public AppointStatusModel getAppointStatusId() {
		return appointStatusId;
	}
	public void setAppointStatusId(AppointStatusModel appointStatusId) {
		this.appointStatusId = appointStatusId;
	}
	public UserModel getAppointHistoryCreateBy() {
		return appointHistoryCreateBy;
	}
	public void setAppointHistoryCreateBy(UserModel appointHistoryCreateBy) {
		this.appointHistoryCreateBy = appointHistoryCreateBy;
	}
	
	
}
