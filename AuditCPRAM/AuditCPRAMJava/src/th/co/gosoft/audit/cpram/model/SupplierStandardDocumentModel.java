package th.co.gosoft.audit.cpram.model;

public class SupplierStandardDocumentModel extends StandardAttributeModel {
	private String supplierStandardDocumentId;	
	private String supplierStandardDocumentLocation;	
	private String supplierStandardDocumentExpireDate;
	private SupplierModel supplierId;	
	private StandardDocumentModel standardDocumentId;
	private UserModel createUser;	
	private UserModel updateUser;
	public String getSupplierStandardDocumentId() {
		return supplierStandardDocumentId;
	}
	public String getSupplierStandardDocumentLocation() {
		return supplierStandardDocumentLocation;
	}
	public SupplierModel getSupplierId() {
		return supplierId;
	}
	public StandardDocumentModel getStandardDocumentId() {
		return standardDocumentId;
	}
	public void setSupplierStandardDocumentId(String supplierStandardDocumentId) {
		this.supplierStandardDocumentId = supplierStandardDocumentId;
	}
	public void setSupplierStandardDocumentLocation(String supplierStandardDocumentLocation) {
		this.supplierStandardDocumentLocation = supplierStandardDocumentLocation;
	}
	public void setSupplierId(SupplierModel supplierId) {
		this.supplierId = supplierId;
	}
	public void setStandardDocumentId(StandardDocumentModel standardDocumentId) {
		this.standardDocumentId = standardDocumentId;
	}
	public String getSupplierStandardDocumentExpireDate() {
		return supplierStandardDocumentExpireDate;
	}
	public void setSupplierStandardDocumentExpireDate(String supplierStandardDocumentExpireDate) {
		this.supplierStandardDocumentExpireDate = supplierStandardDocumentExpireDate;
	}
	public UserModel getCreateUser() {
		return createUser;
	}
	public void setCreateUser(UserModel createUser) {
		this.createUser = createUser;
	}
	public UserModel getUpdateUser() {
		return updateUser;
	}
	public void setUpdateUser(UserModel updateUser) {
		this.updateUser = updateUser;
	}
	
	
}
